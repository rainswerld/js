// First for loop:
for (let i = 0; i < 10; i++) {
  console.log(i)
}

// Similar to this while loop:
let i = 0
while (i < 10) {
  console.log(i)
  i++
}

// Nesting conditionals in loops:
for (let i = 0; i < 10; i++) {
  if (i === 5) {
    console.log('five!')
  } else {
    console.log('nah')
  }
}
